public class Variable01 {

  public static void main(String[] args) {
    int num1; // 宣言
    String str1;
    num1 = 10;
    str1 = "Hello World!"; // 初期化
    System.out.println(num1);
    System.out.println(str1);

    num1 = 20; // 代入
    System.out.println(num1);

    int num2 = 30; // 宣言+初期化
    System.out.println(num2);
  }
}